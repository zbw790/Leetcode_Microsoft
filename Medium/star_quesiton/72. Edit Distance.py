class Solution:
    def minDistance(self, word1: str, word2: str) -> int:
        # 编辑距离（Edit Distance）
        # dp[i][j] = 把 word1 的前 i 个字符变成 word2 的前 j 个字符的最少操作数
        # 三种操作：插入、删除、替换

        m = len(word1)  # word1 长度，对应行
        n = len(word2)  # word2 长度，对应列

        # 创建 (m+1) x (n+1) 的矩阵，+1 是为了包含空字符串的情况
        dp_matrix = [[0] * (n + 1) for _ in range(m + 1)]

        # Base case 1: 第一行，word1 为空 "" → word2[:j]
        # 需要插入 j 次，所以 dp[0][j] = j
        for x in range(1, len(dp_matrix[0])):
            dp_matrix[0][x] = dp_matrix[0][x - 1] + 1

        # Base case 2: 第一列，word1[:i] → word2 为空 ""
        # 需要删除 i 次，所以 dp[i][0] = i
        for y in range(1, len(dp_matrix)):
            dp_matrix[y][0] = dp_matrix[y - 1][0] + 1

        # 填表：从左到右、从上到下遍历
        # x 对应 word2 的进度（列），y 对应 word1 的进度（行）
        for x in range(1, len(dp_matrix[0])):
            for y in range(1, len(dp_matrix)):

                # 当前字符相同，不需要任何操作
                # 问题直接退化为：把 word1[:y-1] 变成 word2[:x-1]
                if word1[y - 1] == word2[x - 1]:
                    dp_matrix[y][x] = dp_matrix[y - 1][x - 1]
                else:
                    dp_matrix[y][x] = min(
                        dp_matrix[y - 1][x - 1] + 1,  # 替换：两边各退一步，把当前字符换掉
                        dp_matrix[y - 1][x] + 1,       # 删除：word1 退一步，删掉 word1 当前字符
                        dp_matrix[y][x - 1] + 1         # 插入：word2 退一步，往 word1 里插一个字符补上
                    )

        # 右下角就是把完整 word1 变成完整 word2 的最少操作数
        return dp_matrix[m][n]


# ============================================================
# 详细 Case 解释（以 word1="ab", word2="ac" 为例）
# ============================================================
#
# 矩阵长这样：
#
#          ""    a     c
#   ""      0    1     2
#   a       1    0     1
#   b       2    1     1
#
# --------------------------------------------------------
# Case 1: dp[2][2] 把 "ab" 变成 "ac"，b ≠ c
#
#   【替换】dp[1][1] + 1 = 0 + 1 = 1
#     dp[1][1] = 把 "a" 变成 "a" = 0步（已经一样了）
#     然后把 b 替换成 c，+1步
#     路径："ab" → 替换b为c → "ac" ✓
#
#   【删除】dp[1][2] + 1 = 1 + 1 = 2
#     dp[1][2] = 把 "a" 变成 "ac" = 1步（插入c）
#     然后 word1 还多一个 b，删掉，+1步
#     路径："ab" → 删b → "a" → 插入c → "ac"（共2步）
#
#   【插入】dp[2][1] + 1 = 1 + 1 = 2
#     dp[2][1] = 把 "ab" 变成 "a" = 1步（删掉b）
#     现在 word2 还差一个 c 没覆盖，插入c，+1步
#     路径："ab" → 删b → "a" → 插入c → "ac"（共2步）
#
#   min(1, 2, 2) = 1，选替换
#
# --------------------------------------------------------
# Case 2: dp[2][1] 把 "ab" 变成 "a"（帮助理解删除）
#
#   b ≠ a
#
#   【删除】dp[1][1] + 1 = 0 + 1 = 1
#     dp[1][1] = 把 "a" 变成 "a" = 0步
#     word1 多出来一个 b，删掉，+1步
#     路径："ab" → 删b → "a" ✓
#
#   这就是删除的本质：
#   word1 的前 y-1 个字符已经能变成 word2 的前 x 个字符了
#   word1 最后多出来的字符没用，删掉就行
#
# --------------------------------------------------------
# Case 3: dp[1][2] 把 "a" 变成 "ac"（帮助理解插入）
#
#   a ≠ c
#
#   【插入】dp[1][1] + 1 = 0 + 1 = 1
#     dp[1][1] = 把 "a" 变成 "a" = 0步
#     word2 还剩一个 c 没覆盖，往 word1 里插入 c，+1步
#     路径："a" → 插入c → "ac" ✓
#
#   这就是插入的本质：
#   word1 的前 y 个字符已经能变成 word2 的前 x-1 个字符了
#   word2 最后还差一个字符，插入补上就行
#
# --------------------------------------------------------
# 核心理解：
#   删除 dp[y-1][x]：word1 多了一个字符 → 删掉它
#   插入 dp[y][x-1]：word2 多了一个字符 → 往 word1 里补一个
#   替换 dp[y-1][x-1]：两边各多一个且不同 → 直接换
#   三种都试，min() 自动选最优，不需要人为判断